    public void salvar(){
        try{
            modelo.setPortCom((String)jComboBox1.getSelectedItem());
            modelo.setBoudRate((String) jComboBox2.getSelectedItem());
            modelo.setAppKey(jTextFieldAppKey.getText());
            modelo.setAppSecret(jTextFieldAppSecret.getText());
            modelo.setToken(jTextFieldToken.getText());
            controle.salvar(modelo);
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"ERRO - "+ ex);
        }
    }
    
    public boolean verificaCamposVazios(){
        JTextField[] jt = {jTextFieldAppKey,jTextFieldAppSecret, jTextFieldToken};
        JComboBox[] jc =  {jComboBox1, jComboBox2};
        for(int i = 0; i < jt.length; i++){
            if(jt[i].getText().equals("")){
                return true;
            }
        }
        for(int j = 0; j< jc.length; j++){
            if(jc[j].getSelectedItem().equals("")){
                return true;
            }
        }
        return false;
    }
    
    public void listarPortas(){
        Enumeration<CommPortIdentifier> listaPortasCom = CommPortIdentifier.getPortIdentifiers();
        while(listaPortasCom.hasMoreElements()){
            CommPortIdentifier identifPorta = listaPortasCom.nextElement();
            jComboBox1.addItem(identifPorta.getName());
        }
    }
    
